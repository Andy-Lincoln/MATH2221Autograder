function[w] = checkWin(M)
N = sum(M,1);
O = sum(M,2);
A = M(1,1) + M(2,2) + M(3,3);
B = M(1,3) + M(2,2) + M(3,1);
if N(1) == 3 | N(2) == 3 | N(3) == 3 | O(1) == 3 | O(2) == 3 | O(3) == 3 | A == 3 | B == 3
  w = 1;
elseif N(1) == -3 | N(2) == -3 | N(3) == -3 | O(1) == -3 | O(2) == -3 | O(3) == -3 | A == -3 | B == -3
else w = 0;
end