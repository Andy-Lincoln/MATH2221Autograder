function w = checkWin(M)
    c = sum(M,1);
    r = sum(M,2);
    d1 = M(1,1) + M(2,2) + M(3,3);
    d2 = M(1,3) + M(2,2) + M(3,1);
    if ((c(1)==3) || (c(2)==3) || (c(3)==3) || (r(1)==3) || (r(2)==3) || (r(3)==3) || (d1==3)|| (d2==3))
        w = 1;
    elseif ((c(1)==-3) || (c(2)==-3) || (c(3)==-3) || (r(1)==-3) || (r(2)==-3) || (r(3)==-3) || (d1==-3)|| (d2==-3))
        w = -1;
    else
        w=0;
    end
    