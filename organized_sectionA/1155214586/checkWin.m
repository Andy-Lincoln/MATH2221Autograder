
function w=checkWin(M)
v=sum(M,1);
s=sum(M,2);
if (v(1,1)==3)||(v(1,2)==3)||(v(1,3)==3)||(s(1,1)==3)||(s(2,1)==3)||(s(3,1)==3)||(sum(diag(M))==3)||(M(1,3)+M(2,2)+M(3,1)==3)
    w=1;
elseif (v(1,1)==-3)||(v(1,2)==-3)||(v(1,3)==-3)||(s(1,1)==-3)||(s(2,1)==-3)||(s(3,1)==-3)||(sum(diag(M))==-3)||(M(1,3)+M(2,2)+M(3,1)==3)
    w=-1;
else w=0;
end
end

    