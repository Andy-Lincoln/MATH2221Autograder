function w=checkWin(M)
u=sum(M,1);
v=sum(M,2);
if (u(1,1)==3)||(u(1,2)==3)||(u(1,3)==3)||(v(1,1)==3)||(v(2,1)==3)||(v(3,1)==3)||(sum(diag(M))==3)||(M(1,3)+M(2,2)+M(3,1)==3);
    w=1;
elseif (u(1,1)==-3)||(u(1,2)==-3)||(u(1,3)==-3)||(v(1,1)==-3)||(v(2,1)==-3)||(v(3,1)==-3)||(sum(diag(M))==-3)||(M(1,3)+M(2,2)+M(3,1)==-3)
    w=-1;
else w=0;
end
end