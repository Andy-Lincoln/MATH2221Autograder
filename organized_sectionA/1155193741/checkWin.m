function[w]=checkWin(M)
    s=sum(M,1);
    r=sum(M,2);
    diag1 = M(1,1) + M(2,2) + M(3,3);
    diag2 = M(1,3) + M(2,2) + M(3,1);
    if s(:,1)==3 || s(:,2)==3 || s(:,3)==3 || r(1,:)==3 || r(2,:)==3 || r(3,:)==3 || diag1==3 || diag2==3
        w = 1;
    elseif s(:,1)==-3 || s(:,2)==-3 || s(:,3)==-3 || r(1,:)==-3 || r(2,:)==-3 || r(3,:)==-3 || diag1==-3 || diag2==-3
        w = -1;
    else
        w = 0;
    end